CREATE TABLE ACTOR (
ID INT (3),
NAME VARCHAR (20) NOT NULL,
GENDER CHAR (1),
PRIMARY KEY (ID)
);

CREATE TABLE DIRECTOR (
ID INT (3),
NAME VARCHAR (20) NOT NULL,
PHONE INT (10),
PRIMARY KEY (ID)
);

CREATE TABLE MOVIES (
ID INT (4),
TITLE VARCHAR (20) NOT NULL,
YEAR INT (4),
LANG VARCHAR (12),
DIR_ID INT (3),
PRIMARY KEY (ID),
FOREIGN KEY (DIR_ID) REFERENCES DIRECTOR (ID)
);

CREATE TABLE MOVIE_CAST (
ACT_ID INT (3),
MOV_ID INT (4),
ROLE VARCHAR (10),
PRIMARY KEY (ACT_ID, MOV_ID),
FOREIGN KEY (ACT_ID) REFERENCES ACTOR (ID),
FOREIGN KEY (MOV_ID) REFERENCES MOVIES (ID)
);

CREATE TABLE RATING (
MOV_ID INT (4),
REV_STARS INTEGER NOT NULL,
PRIMARY KEY (MOV_ID),
FOREIGN KEY (MOV_ID) REFERENCES MOVIES (ID)
);