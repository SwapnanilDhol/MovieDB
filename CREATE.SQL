CREATE TABLE ACTOR (
ID NUMBER (3),
NAME VARCHAR (20),
GENDER CHAR (1),
PRIMARY KEY (ID)
);

CREATE TABLE DIRECTOR (
ID NUMBER (3),
NAME VARCHAR (20),
PHONE NUMBER (10),
PRIMARY KEY (DIR_ID)
);

CREATE TABLE MOVIES (
ID NUMBER (4),
TITLE VARCHAR (20),
YEAR NUMBER (4),
LANG VARCHAR (12),
DIR_ID NUMBER (3),
PRIMARY KEY (MOV_ID),
FOREIGN KEY (DIR_ID) REFERENCES DIRECTOR (ID)
);

CREATE TABLE MOVIE_CAST (
ACT_ID NUMBER (3),
MOV_ID NUMBER (4),
ROLE VARCHAR (10),
PRIMARY KEY (ACT_ID, MOV_ID),
FOREIGN KEY (ACT_ID) REFERENCES ACTOR (ID),
FOREIGN KEY (MOV_ID) REFERENCES MOVIES (ID)
);

CREATE TABLE RATING (
MOV_ID NUMBER (4),
REV_STARS INTEGER,
PRIMARY KEY (MOV_ID),
FOREIGN KEY (MOV_ID) REFERENCES MOVIES (ID)
);