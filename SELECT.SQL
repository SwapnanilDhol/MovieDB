/*Group by. Aggregation with group by*/
SELECT DISTINCT TITLE
FROM MOVIES M, MOVIE_CAST C
WHERE M.ID=C.MOV_ID AND
	ACT_ID IN (SELECT ACT_ID
		   FROM MOVIE_CAST
		   GROUP BY ACT_ID
		   HAVING COUNT(ACT_ID) > 1);

